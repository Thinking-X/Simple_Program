;统计一组字节数据中的正数个数和0的个数，结果存于A1和A2单元
DATA SEGMENT
NUM 	DB	2,-2,4,0,5,0,-3,0,9
A1	DB	?
A2	DB	?
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE

START:  MOV AX, DATA
	MOV DS, AX

	MOV BX, OFFSET NUM
	MOV DL, 0		;DL记录0出现的次数
	MOV DH, 0		;DH记录正数出现的次数
	MOV CX, 9		;CX表示数的个数
 LP:	MOV AL, [BX]
	CMP AL, 0
	JG  LP1			;可能有负数，所以用JG
	JZ  LP2
	JMP NEXT
 LP1:	INC DH 
	JMP NEXT
 LP2:	INC DL

 NEXT:	INC BX
	LOOP LP

	MOV A1, DH
	MOV A2, DL

	ADD A1, 30H
	MOV DL, A1
	MOV AH, 02H
	INT 21H

	ADD A2, 30H
	MOV DL, A2
	MOV AH, 02H
	INT 21H

	MOV AH, 4CH
	INT 21H
CODE ENDS
END START