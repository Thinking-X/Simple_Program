DATA SEGMENT
NUM	DB	7,-7,2,9,0,-4,-1,8,5
COUNT	EQU	$-NUM;	COUNT	DB	$-NUM
MAX	DB	?
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE
 START: MOV AX, DATA
	MOV DS, AX

	MOV CL, COUNT-1
	MOV CH, 00H
	MOV BX, OFFSET NUM
	MOV AL, [BX]
 LP:	INC BX
	CMP AL, [BX]
	JG  NEXT
	MOV AL, [BX]
 NEXT:	LOOP LP
	MOV MAX, AL

	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START