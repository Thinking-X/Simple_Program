DATA SEGMENT
STR	DB	'ABCDEFG*'
NUM	DB	?
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE
START:  MOV AX, DATA
	MOV DS, AX
	LEA BX, STR
	MOV DL, 0
	
 LP:	MOV AL, [BX]
	CMP AL, '*'
	JZ  NEXT
	INC DL
	INC BX
	JMP LP
	MOV NUM, DL
 NEXT:	ADD DL, 30H 
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START