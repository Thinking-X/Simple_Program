;已知一个带符号的8位数存于NUM单元，
;若NUM小于0，则将0FFH送MA单元，
;若NUM等于0，则将0FFH送MA+1单元，
;若NUM大于0，则将0FFH送MA+2单元
;并显示出在屏幕上

DATA SEGMENT
NUM	DB	10010010B
MA	DB	3	DUP(?)
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE

START:  MOV AX, DATA
	MOV DS, AX

	MOV AL, NUM
	AND AL, AL
	JS  LP1
	JZ  LP2

	MOV [MA+2], 0FFH
	JMP DONE

 LP1:	MOV [MA], 0FFH
	JMP DONE

 LP2:	MOV [MA+1], 0FFH

 DONE:	MOV AH, 4CH
	INT 21H
CODE ENDS
END START