;在数据区有一长度为15字节的字符串，起始地址为STR，统计字符串中'a'个数，并存于NUM单元
DATA SEGMENT
STR	DB	'I am teacher'
NUM	DB	?
DATA ENDS

CODE SEGMENT
	ASSUME DS: DATA, CS:CODE
START:  MOV AX, DATA
	MOV DS, AX
	
	MOV DL, 0	
	MOV CX, 15
	LEA BX, STR

 LP:	MOV AL, [BX]
	CMP AL, 'a'
	JNZ NEXT
	INC DL
 NEXT:	INC BX
	LOOP LP
	
	MOV NUM, DL
	ADD NUM, 30H
	MOV DL, NUM
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START