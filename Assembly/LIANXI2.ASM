;统计数组STR单元起长度为10个字节的有符号数中负数的个数，送如COUNT单元
DATA SEGMENT
STR 	DB	2,-2,4,0,5,0,-3,0,9
COUNT	DB	?
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE

START:  MOV AX, DATA
	MOV DS, AX

	MOV BX, OFFSET STR
	MOV DL, 0		;DL记录0出现的次数
	MOV CX, 9		;CX表示数的个数

 LP:	MOV AL, [BX]
	CMP AL, 0
	JGE  LP1		;AL<=0时，跳转
	INC DL
 LP1:	INC BX
	LOOP LP

	MOV COUNT, DL

	ADD COUNT, 30H
	MOV DL, COUNT
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START